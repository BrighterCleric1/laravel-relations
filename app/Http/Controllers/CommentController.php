    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Article $articles, Request $request)
    {
        $request->validate([
            'user' => 'required | string',
            'contentComment' => 'required | string',
        ]);

        $data = $request->all();


        $newComment = new Comment();
        $newComment->user = $data['user'];
        $newComment->contentComment = $data['contentComment'];
        $newComment->article_id = $articles->id;
        $newComment->save();


        return redirect()->route('articles.show', $articles);
    }

